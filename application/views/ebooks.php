$("#freeforms").submit(function(event) {
        event.preventDefault();
        $.ajax({
                type: "POST", 
                url: '<?php echo base_url('Admin/Ebook/add');?>',
                dataType:'json',
                data: new FormData(this), 
                contentType: false,
                cache: false,
                processData:false,
                beforeSend:function()
                {},
                success:function(responce)
                {
                  if(responce.status==0)
                  {
                   toastr.error(responce.message);
                  }else if(responce.status==1)
                  {
                     toastr.success(responce.message);
                    setTimeout(location.reload.bind('<?php echo base_url('Ebooks');?>'), 20000);
                  }  
                },
                error:function()
                {
                  toastr.error('Something Went Wrong..');
                },
                complete:function()
                {
                }
            });
    })

